Apply and Lambda 1:
a -> a b
(a:? -> (a:? b:?):?):?
1:-------------------
(a:? -> (a:? b:?):?):'1
(a:'2 -> (a:? b:?):?):'1                          : Bind(a:'2)
(a:'2 -> (a:? b:?):'3):'1                         : Memoize('1 => ('2 -> '3))
(a:'2 -> (a:? b:'4):'3):'1
(a:'2 -> (a:('4 -> '3) b:'4):'3):'1               : Lookup(a => '2), Memoize('2 => ('4 -> '3)))
2:-------------------
(a:('4 -> '3) -> (a:('4 -> '3) b:'4):'3):'1       : Update('2 => ('4 -> '3)))
(a:('4 -> '3) -> (a:('4 -> '3) b:'4):'3):('2 -> '3)       : Update('1 => ('2 -> '3))
(a:('4 -> '3) -> (a:('4 -> '3) b:'4):'3):(('4 -> '3) -> '3)       : Update('2 => ('4 -> '3)))
3:-------------------
('4 -> '3) -> '3

